<script lang="ts">
  import { onMount } from 'svelte';
  import AnalyticsDashboard from './AnalyticsDashboard.svelte';
  
  export let users: any[] = [];
  
  let stats = {
    totalUsers: 0,
    adminUsers: 0,
    regularUsers: 0,
    verifiedUsers: 0,
    totalSessions: 0,
    activeSessions: 0,
    totalConversations: 0,
    totalMessages: 0
  };
  
  let loading = true;
  
  // Calculate basic user stats
  $: {
    stats.totalUsers = users.length;
    stats.adminUsers = users.filter(u => u.role === 'admin').length;
    stats.regularUsers = users.filter(u => u.role === 'user').length;
    stats.verifiedUsers = users.filter(u => u.emailVerified).length;
  }
  
  onMount(async () => {
    try {
      // Fetch additional stats from API
      const response = await fetch('/admin/api/stats');
      if (response.ok) {
        const data = await response.json();
        stats = { ...stats, ...data };
      }
    } catch (error) {
      console.error('Error fetching stats:', error);
    } finally {
      loading = false;
    }
  });
  
  const statCards = [
    {
      title: 'Total Users',
      value: () => stats.totalUsers,
      icon: 'M12 4.354a4 4 0 110 5.292M15 21H3v-1a6 6 0 0112 0v1zm0 0h6v-1a6 6 0 00-9-5.197m13.5-9a2.5 2.5 0 11-5 0 2.5 2.5 0 015 0z',
      color: 'blue',
      description: 'Registered users'
    },
    {
      title: 'Admin Users',
      value: () => stats.adminUsers,
      icon: 'M9 12l2 2 4-4m5.618-4.016A11.955 11.955 0 0112 2.944a11.955 11.955 0 01-8.618 3.04A12.02 12.02 0 003 9c0 5.591 3.824 10.29 9 11.622 5.176-1.332 9-6.03 9-11.622 0-1.042-.133-2.052-.382-3.016z',
      color: 'red',
      description: 'System administrators'
    },
    {
      title: 'Regular Users',
      value: () => stats.regularUsers,
      icon: 'M16 7a4 4 0 11-8 0 4 4 0 018 0zM12 14a7 7 0 00-7 7h14a7 7 0 00-7-7z',
      color: 'green',
      description: 'Standard users'
    },
    {
      title: 'Verified Users',
      value: () => stats.verifiedUsers,
      icon: 'M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z',
      color: 'purple',
      description: 'Email verified'
    },
    {
      title: 'Active Sessions',
      value: () => stats.activeSessions,
      icon: 'M8.111 16.404a5.5 5.5 0 017.778 0M12 20h.01m-7.08-7.071c3.904-3.905 10.236-3.905 14.141 0M1.394 9.393c5.857-5.857 15.355-5.857 21.213 0',
      color: 'indigo',
      description: 'Currently online'
    },
    {
      title: 'Total Sessions',
      value: () => stats.totalSessions,
      icon: 'M12 8v4l3 3m6-3a9 9 0 11-18 0 9 9 0 0118 0z',
      color: 'yellow',
      description: 'All login sessions'
    },
    {
      title: 'Conversations',
      value: () => stats.totalConversations,
      icon: 'M8 12h.01M12 12h.01M16 12h.01M21 12c0 4.418-4.03 8-9 8a9.863 9.863 0 01-4.255-.949L3 20l1.395-3.72C3.512 15.042 3 13.574 3 12c0-4.418 4.03-8 9-8s9 3.582 9 8z',
      color: 'pink',
      description: 'AI chat conversations'
    },
    {
      title: 'Chat Messages',
      value: () => stats.totalMessages,
      icon: 'M7 8h10M7 12h4m1 8l-4-4H5a2 2 0 01-2-2V6a2 2 0 012-2h14a2 2 0 012 2v8a2 2 0 01-2 2h-3l-4 4z',
      color: 'cyan',
      description: 'Total messages sent'
    }
  ];
  
  function getColorClasses(color: string) {
    const colors = {
      blue: 'bg-blue-500/20 text-blue-400',
      red: 'bg-red-500/20 text-red-400',
      green: 'bg-green-500/20 text-green-400',
      purple: 'bg-purple-500/20 text-purple-400',
      indigo: 'bg-indigo-500/20 text-indigo-400',
      yellow: 'bg-yellow-500/20 text-yellow-400',
      pink: 'bg-pink-500/20 text-pink-400',
      cyan: 'bg-cyan-500/20 text-cyan-400'
    };
    return colors[color] || colors.blue;
  }
</script>

<div class="space-y-8">
  <!-- Page Title -->
  <div>
    <h2 class="text-2xl font-bold text-white mb-2">System Overview</h2>
    <p class="text-gray-400">Real-time insights into your application's performance and user activity</p>
  </div>

        <!-- Statistics Grid -->
   <div class="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-6">
     {#each statCards as card}
       <div class="card p-6 hover:bg-gray-750 transition-colors">
         <div class="flex items-center">
           <div class="p-3 rounded-lg {getColorClasses(card.color)}">
             <svg class="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
               <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d={card.icon}></path>
             </svg>
           </div>
           <div class="ml-4 flex-1">
             <p class="text-sm font-medium text-gray-400">{card.title}</p>
             <p class="text-2xl font-semibold text-white">
               {#if loading}
                 <span class="animate-pulse">--</span>
               {:else}
                 {card.value().toLocaleString()}
               {/if}
             </p>
             <p class="text-xs text-gray-500 mt-1">{card.description}</p>
           </div>
         </div>
       </div>
     {/each}
   </div>

   <!-- Analytics Charts -->
   <div class="analytics-compact">
     <AnalyticsDashboard />
   </div>

    
</div>

